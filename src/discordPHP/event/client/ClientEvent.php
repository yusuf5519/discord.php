declare(strict_types=1);

namespace discordPHP\event\client;

use discordPHP\discord\user\Client;
use discordPHP\event\Event;

abstract class ClientEvent extends Event{

    /** @var Client */
    protected $client;

    /**
     * @return Client
     */
    public function getClient() : Client{
        return $this->client;
    }

}